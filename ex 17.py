#Como descobrir seno cosseno e tangente de um ângulo


import math 

angulo = int(input('Poderia me dar o valor de algum ângulo? '))
radianos = math.radians(angulo)
seno = math.sin(radianos)
cosseno = math.cos(radianos)
tangente = math.tan(radianos)

print(f'O seno do seu ângulo é: {seno} o cosseno é: {cosseno} e o tangente é {tangente} e Obrigado por participar da pesquisa. ')
